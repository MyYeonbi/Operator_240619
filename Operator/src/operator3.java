public class operator3 {
    public static void main(String[] args) {
        int sum3 = 2 * 2 + 3 * 3; //(2 * 2) + (3 * 3)
        int sum4 = (2 * 2) + (3 * 3); // sum3 같다.
        System.out.println("sum3: " + sum3);
        System.out.println("sum4: " + sum4);
        //복잡할경우 그냥 괄호 넣는게 명확함.
    }
}
